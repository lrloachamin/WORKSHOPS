package ec.espe.edu.ec.arrrays;
import java.util.Scanner;

/**
 *
 * @author Asus
 */
public class Person {
    
    private String day;
    private String month;
    private String year;
    private String name;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Person(String day, String month, String year, String name) {
        this.day = day;
        this.month = month;
        this.year = year;
        this.name = name;
    }

      
    
    

    

    
        
        
        
    

   
    
}
