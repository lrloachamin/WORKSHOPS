  public void calcualte(){
  
    try{
     
        float Vo = 0;
        float angule = 0;
    
        Vo=Float.parseFloat(this.jTextField1.getText());
        angule=Float.parseFloat(this.jTextField2.getText());
        FileManager.createFileWorker(workers, file);
        max=Calculate.distanceMax(Vo, angule);
        Calculate calculate = new Calculate(Vo,angule,max);
        this.workers.add(calculate);
        FileManager.FileWorker(file, workers);
        workers.clear();
        
    }catch(Exception e){
        showMessage("Error");
       }
    }
